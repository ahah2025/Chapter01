package com.javaex.ex01;

public class Ex02 {
	
	public static void main(String[] args) {
		
		/*
		double var01;
		var01 = 3.14;
		*/
		double var01 = 3.14;
		
		float var02;
		var02 =3.14F; //float 형은 항상 F를 붙어야 한다.

		System.out.println(var01); //'var01'은 개발자가 이름 정하는 것
		System.out.println(var02); //'var02'은 개발자가 이름 정하는 것
		
		/********************************/
		double var03 = 0.1234567890123456789;
		float var04 = 0.1234567890123456789F;
		
		System.out.println(var03);
		System.out.println(var04);		
	}
}